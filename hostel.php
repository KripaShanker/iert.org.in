<p>A Spacious Hostel is situated close to the Institution Campus at the bank of Ganges. I.E.R.T. with its beautifully landscaped and carefully tended gardens provides an ideal environment for a congenial learning experience. Here is a campus that invites students out of their hostels to spend time between classes midst greenery and a riot of floral colour. All rooms are provided with ceiling fans, bookracks & wardrobes. Also each student residing in the hostel is given a bed, table and chair.</p>

