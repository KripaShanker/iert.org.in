Newly admitted students in the Institute can get hostel admission. The selection is made on the basis of one's merit, in the entrance test & participation in extra & co-curricular activities. The First Year students will have to apply to the Warden on the prescribed form for hostel admission at the time of admission. Seats are reserved for S.C./S.T. candidates as per the norms. Proper security measures are implemented for the welfare of the students.</p>


